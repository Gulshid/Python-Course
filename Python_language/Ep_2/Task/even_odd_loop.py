#2. Find even & odd numbers using if condition inside loop

# Even Numbers
print("Even Numbers :")
for x in range(0, 21):
    if x % 2 == 0:
        print(x, end = " ")
        
print()
# Odd Numbers
print("Odd Numbers :")
for y in range(0 , 21):
    if y % 2 == 1:
        print(y , end= " ")

print()