#    *** Chapter 6: File Handling in Python ***

# 1. File Handling
#    File handling is used to store and manage data permanently in files.

# 2. Types of Files
#    Common files include text files, CSV files, PDF files, and log files.

# 3. Need of File Handling
#    Used for saving records, logs, and reports.

# 4. Opening a File
#    Files are opened using the open() function.

# 5. File Modes
#    r (read), w (write), a (append), x (create), + (read/write).

# 6. Writing Files
#    Data is written using write() and writelines().

# 7. Reading Files
#    Data is read using read(), readline(), and readlines().

# 8. Appending Files
#    Append mode adds data at the end without deleting old data.

# 9. Closing a File
#    close() is used to save and close the file.

# 10. with Statement
#     Automatically closes the file after use.

# 11. File Modification
#     Files are modified by reading and rewriting.
