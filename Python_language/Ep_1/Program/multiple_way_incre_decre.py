# Multiple way to Increment and Decrement in python
value = 5
print(value)
#  method 1 for Increment and  decremnt 
value +=1
print(value) # value = 6

value -=1
print(value) # value = 5

# method 2 for Increment and Decrement
value = value + 1
print(value)  # value = 6

value = value - 1
print(value)   # value = 5
