# 3. How to Sub Two Number with and Without Third Variable
a = 3
b = 9

# Method 1. Without third 
print(f"The Subtraction of two value is : {a - b}")

# Method 2. With Third Variable

c = a - b
print(f"The Subtraction of two value is : {c}")


