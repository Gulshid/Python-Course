# Task 6: Display first, middle, last element (Tuple)
tuple_num = (2,5,6,7,8,10,12)

print("Tuple element :")
print(tuple_num)

print("************")
# first element in tuple 
first_Element = tuple_num[0]

# middle element in tuple 
middle_Element = tuple_num[len(tuple_num) // 2]

# last element in tuple 
Last_Element = tuple_num[-1]


print(f"First ELement in tuple : {first_Element}")
print(f"Middle Element in tuple : {middle_Element}")
print(f"Last Element in tuple : {Last_Element}")