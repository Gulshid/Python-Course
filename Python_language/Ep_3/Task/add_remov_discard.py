# Task 2: Add/remove elements (add, remove, discard)
my_set = {2,4,6}

print("Set values :")
print(my_set)

print("************")
print("add Operation in set :")
my_set.add(8)
print(my_set)

print("************")
print("Remove Operation in set : ")
my_set.remove(4)
print(my_set)

print("************")
print('Discard Operation in set :')
my_set.discard(10)
print(my_set)