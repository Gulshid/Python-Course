# 2️ TUPLE
# ✔ Definition:

# A tuple is an ordered, unchangeable (immutable) collection.

# ✔ Syntax:
# my_tuple = (10, 20, 30, 40)

# ✔ Features:

# Ordered
x = (1,2,3)

# Immutable (cannot change after creation)

# Faster than lists

# Allows duplicates
y = (1,1,2,3,5,3,2)

# ✔ Access:
# t = (1, 2, 3)
# print(t[0])      # accessing element

# ✔ Why use tuples?

# Data should not change

# Faster performance

# Secure data storage