#Exception Handling:
    # Exception handling is a programming technique used
    # to detect and handle runtime errors so that a
    # program can continue running without crashing.
    
    
#  Why We Use Exception Handling ?:

        # We use exception handling to:

        # 1. Prevent program crash when an error occurs

        # 2. Handle runtime errors safely (like division by zero, invalid input)

        # 3. Maintain normal program flow

        # 4. Display user-friendly error messages

        # 5. Make programs more reliable and robust