# Arthematic and Assignment Operators
#  +=, -=, *=, /=, %=, =
# = ===> Assign
value = 3
print("= Operator :",value)

# += ===> Additon and Assignment Operator
value += 2 # 3 + 2 =  value = 5
print("+= Operator :", value)

# -= ===> Subtraction and Assignment Operator
value -= 3 # 5 - 3 => value = 2
print("-= Operator :", value)

# *= ===> Multiplication and Assignment Operator
value *= 6 # 2 * 6 => value = 12
print("*= Operator :", value)


# /= ===> Division and Assignment Operator
value /= 3 # 12 / 3 => 4
print("/= Operator :", value)

# %= ===> Division and Assignment Operator
value %= 2 # 4 / 2 => reminder = 0
print("%= Operator :", value)
