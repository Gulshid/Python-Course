# Arthematic Operator 
val_1 = input("Enter for Value 1 :")
val_2 = input("Enter for Value 2 : ")

# +, - , * ,/ , %
# Addition +
add = val_1 + val_2

# Subtraction -
sub = val_1 - val_2

# Multiplication *
mul = val_1 * val_2

# Division /
div = val_1 / val_2

# modulas %
mod = val_1 % val_2


# display the Arthematic Operation
print("*** Arthematic Operation ***")
print("Addition of two value :", add)
print("Subtraction of two value :", sub)
print("Multiplication of two value :", mul)
print("Division of two value :", div)
print("Modulas of two value :", mod)