# Arthematic Operator 
val_1 = int(input("Enter the value for val_1:"))
val_2 = int(input("Enter the value for val_2:"))

# +, - , * ,/ , %
# Addition +
add = val_1 + val_2

# Subtraction -
sub = val_1 - val_2

# Multiplication *
mul = val_1 * val_2

# Division /
div = val_1 / val_2

# modulas %
mod = val_1 % val_2



# display the Arthematic Operation
print("*** Arthematic Operation ***")
print("The Addition of two value is :", add)
print("The Subtraction of two value is :", sub)
print("The Multiplication of two value is :", mul)
print("The Division of two value is :", div)
print("The Modulas of two value is :", mod)

