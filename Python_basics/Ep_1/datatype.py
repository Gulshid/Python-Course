# Datatype in python

# int ---> Numeric value
int_value = 2
# float ---> Numeric decimal value 
float_value = 3.4

# string / char ---> '', ""
string_value = "Hello WOrld"

# bool --> true, false
istrue = True
isfalse = False

# List ---> [    ]
list_value = [1,2,3,4]

# Tuple ---> (     )
tuple_value = (1,2,3,4,5)

# set ---> {. }
set_value = {1,2,34,56}

# dict ---> { key : value}
dict_value = {
    'Name ': "Gulshid Zada",
    'Roll NO': 391,
    'Semester' : 5
}

# display these all Datatype
print("Int :",int_value)
print("Float :", float_value)
print("String :", string_value)
print("Bool True:", istrue)
print("Bool False:", isfalse)
print("List :", list_value)
print("Tuple :", tuple_value)
print("Set :", set_value)
print("Dict :", dict_value)

# find the type of variable
print(type(int_value))
print(type(float_value))
print(type(string_value))
print(type(istrue))
print(type(isfalse))
print(type(list_value))
print(type(tuple_value))
